import React, { useState, useEffect } from 'react';
import { Navbar, Nav, Container, Spinner } from 'react-bootstrap';
import 'bootstrap/dist/css/bootstrap.min.css';
import DonationList from './DonationList';
import VotingList from './VotingList';
import RealEstateWaqf from './realEstateWaqf';
import Head from 'next/head';
import Wallet from './wallet';
import Documentation from './documentation';

const App: React.FC = () => {
  const [currentView, setCurrentView] = useState<string>('home');
  const [expanded, setExpanded] = useState<boolean>(false);
  const [loading, setLoading] = useState(true);

  const handleNavSelect = (selectedKey: string | null) => {
    if (selectedKey) {
      setCurrentView(selectedKey);
      setExpanded(false);
    }
  };

  useEffect(() => {
    setTimeout(() => {
      setLoading(false);
    }, 2000);
  }, []);

  return (
    <div style={{ fontFamily: 'Arial, sans-serif', backgroundColor: '#E4C59E', color: '#333' }}>
      <Head>
        <title>Lagoon Dapps</title>
        <meta content="Generated by @rainbow-me/create-rainbowkit" name="description" />
        <link href="/images/logo icon only green.png" rel="icon" />
      </Head>

      <header style={{ position: 'sticky', top: 0, zIndex: 1000, backgroundColor: '#E4C59E' }}>
        <Container fluid>
          <Navbar expand="lg" style={{ padding: '10px 20px' }} expanded={expanded}>
            <Navbar.Brand style={{ color: '#fff', fontWeight: 'bold', display: 'flex', alignItems: 'center', cursor: 'pointer' }}>
              <img src="/images/logo landscape lagoon white.png" alt="Lagoon Logo" style={{ height: '50px', marginRight: '10px', transition: 'transform 0.3s ease' }}
                   onMouseOver={e => e.currentTarget.style.transform = 'scale(1.1)'}
                   onMouseOut={e => e.currentTarget.style.transform = 'scale(1)'}/>

            </Navbar.Brand>
            <Navbar.Toggle aria-controls="basic-navbar-nav" onClick={() => setExpanded(!expanded)} />
            <Navbar.Collapse id="basic-navbar-nav" className="justify-content-end">
              <Nav activeKey={currentView} onSelect={handleNavSelect}>
                {['home', 'donation-list', 'voting-list', 'asset-tokenization', 'documentation'].map((key, index) => (
                  <Nav.Link
                    key={key}
                    eventKey={key}
                    style={{ color: '#fff', padding: '10px 20px', margin: '0 10px', fontWeight: 'bold',
                      transition: 'color 0.3s ease, background-color 0.3s ease', borderRadius: '5px',
                    }}
                    onMouseOver={e => {
                      e.currentTarget.style.backgroundColor = '#5A9367';
                      e.currentTarget.style.color = '#fff';
                    }}
                    onMouseOut={e => {
                      e.currentTarget.style.backgroundColor = 'transparent';
                      e.currentTarget.style.color = '#fff';
                    }}
                  >
                    {index === 0 ? 'Home' : `Lagoon ${key.split('-').map(word => word.charAt(0).toUpperCase() + word.slice(1)).join(' ')}`}
                  </Nav.Link>
                ))}
              </Nav>
              <div style={{ marginLeft: 'auto' }}>
                <div onClick={() => setExpanded(false)}>
                  <Wallet />
                </div>
              </div>
            </Navbar.Collapse>
          </Navbar>
        </Container>
      </header>

      <div>
        <main style={{ padding: '60px 20px', minHeight: '80vh' }}>
          {loading ? (
            <div style={{ display: 'flex', justifyContent: 'center', alignItems: 'center', height: '100%' }}>
              <Spinner animation="border" variant="success" />
            </div>
          ) : currentView === 'home' ? (
            <section style={{ 
              display: 'flex', alignItems: 'center', justifyContent: 'left',
              backgroundColor: '#E4C59E', color: '#fff', padding: '40px', borderRadius: '10px', 
              marginBottom: '40px', flexDirection: 'column' }}>
              <img src="/images/decentral_logo-removebg-preview.png"  alt="Lagoon Image" 
                style={{ maxWidth: '40%', marginRight: '20px', transition: 'transform 0.3s ease', 
                cursor: 'pointer', width: '100%', maxHeight: '500px',}}
                onMouseOver={e => e.currentTarget.style.transform = 'scale(1.1)'}
                onMouseOut={e => e.currentTarget.style.transform = 'scale(1)'}
              />
              <div style={{ textAlign: 'center', marginTop: '20px', fontSize: '1rem' }}>
                <h1 style={{ fontSize: '2.5rem', marginBottom: '20px', fontWeight: 'bold'}}>
                  Lagoon Dapps
                </h1>
                <p style={{ fontSize: '1.25rem' }}>
                  Your Decentralized Waqf Tokenization and Charity
                </p>
              </div>
            </section>
          ) : currentView === 'donation-list' ? (
            <DonationList />
          ) : currentView === 'voting-list' ? (
            <VotingList />
          ) : currentView === 'asset-tokenization' ? (
            <RealEstateWaqf />
          ) : currentView === 'documentation' ? (
            <Documentation />
          ) : null}
        </main>
      </div>

      <footer style={{ backgroundColor: '#E4C59E', color: '#fff', padding: '20px 0', textAlign: 'center' }}>
        <p style={{ marginBottom: 0 }}>&copy; 2024 Lagoon Dapps. All rights reserved.</p>
      </footer>
    </div>
  );
};

export default App;
